<p>PORUS is a portable <a href="http://www.usb.org">USB</a> stack for USB peripherals.  It handles enumeration and many standard control requests, and provides a portable API for performing USB transactions.</p>

<p>See the <a href="faq.php">FAQ</a> for a quick introduction to PORUS.</p>

<p>PORUS is distributed as source only.  (This isn't because I'm a source-code fanatic; it's because it's not practical to distribute PORUS as a binary.)  Download it from the
<a href="http://developer.berlios.de/project/showfiles.php?group_id=7232">files page</a>.</p>

